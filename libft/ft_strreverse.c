#include "libft.h"

void	ft_strreverse(char *s)
{
	size_t	i;
	size_t	j;
	char	bfr;

	i = 0;
	j = ft_strlen(s) - 1;
	while (i < j)
	{
		bfr = s[i];
		s[i] = s[j];
		s[j] = bfr;
		j--;
		i++;
	}
}
